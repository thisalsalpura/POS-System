    private void addtogrnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addtogrnActionPerformed

        // add the grn to the table
        String employee_email = jLabel3.getText();
        String grn_number = jLabel6.getText();
        String supplier_email = jLabel5.getText();
        String supplier_name = jLabel9.getText();
        String material_id = jLabel11.getText();
        String material_name = jLabel15.getText();
        String grn_material_qty = qty.getText();
        String grn_material_price = unit_price.getText();

        if (employee_email.isEmpty()) {
            Notifications.getInstance().show(Notifications.Type.ERROR, Notifications.Location.TOP_RIGHT, 3500l, "Something went wrong!");
        } else if (grn_number.isEmpty()) {
            Notifications.getInstance().show(Notifications.Type.ERROR, Notifications.Location.TOP_RIGHT, 3500l, "Something went wrong!");
        } else if (supplier_email.isEmpty()) {
            Notifications.getInstance().show(Notifications.Type.ERROR, Notifications.Location.TOP_RIGHT, 3500l, "Please select a Supplier!");
        } else if (supplier_name.isEmpty()) {
            Notifications.getInstance().show(Notifications.Type.ERROR, Notifications.Location.TOP_RIGHT, 3500l, "Please select a Supplier!");
        } else if (material_id.isEmpty()) {
            Notifications.getInstance().show(Notifications.Type.ERROR, Notifications.Location.TOP_RIGHT, 3500l, "Please select a Material!");
        } else if (material_name.isEmpty()) {
            Notifications.getInstance().show(Notifications.Type.ERROR, Notifications.Location.TOP_RIGHT, 3500l, "Please select a Material!");
        } else if (grn_material_qty.isEmpty() || grn_material_qty.equals("0") || grn_material_qty.equals("0.00")) {
            Notifications.getInstance().show(Notifications.Type.ERROR, Notifications.Location.TOP_RIGHT, 3500l, "Please enter the GRN Material Quantity!");
        } else if (!grn_material_qty.matches("^[0-9]+(\\.[0-9]{1,2})?$")) {
            Notifications.getInstance().show(Notifications.Type.ERROR, Notifications.Location.TOP_RIGHT, 3500l, "Invalid GRN Material Quantity!");
        } else if (grn_material_price.isEmpty() || grn_material_price.equals("0") || grn_material_price.equals("0.00")) {
            Notifications.getInstance().show(Notifications.Type.ERROR, Notifications.Location.TOP_RIGHT, 3500l, "Please enter the GRN Material Unit Price!");
        } else if (!grn_material_price.matches("^[0-9]+(\\.[0-9]{1,2})?$")) {
            Notifications.getInstance().show(Notifications.Type.ERROR, Notifications.Location.TOP_RIGHT, 3500l, "Invalid GRN Material Price!");
        } else {

            selectSupplier.setEnabled(false);

            try {

                double grn_qty = Double.parseDouble(grn_material_qty);
                double grn_price = Double.parseDouble(grn_material_price);

                boolean MaterialExists = false;

                DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();

                for (int i = 0; i < dtm.getRowCount(); i++) {
                    if (String.valueOf(dtm.getValueAt(i, 1)).equals(material_id)) {

                        double existingQty = Double.parseDouble(String.valueOf(dtm.getValueAt(i, 3)));

                        double updatedQty = existingQty + grn_qty;
                        double updatedPrice = grn_price;

                        double updatedTotal = updatedQty * updatedPrice;

                        dtm.setValueAt(String.valueOf(updatedQty), i, 3);
                        dtm.setValueAt(String.valueOf(updatedPrice), i, 4);
                        dtm.setValueAt(String.valueOf(updatedTotal), i, 5);

                        MaterialExists = true;
                        break;
                    }
                }

                if (!MaterialExists) {

                    Vector<String> vector = new Vector<>();
                    vector.add(supplier_email);
                    vector.add(material_id);
                    vector.add(material_name);
                    vector.add(String.valueOf(grn_qty));
                    vector.add(String.valueOf(grn_price));
                    vector.add(String.valueOf(grn_qty * grn_price));

                    dtm.addRow(vector);

                }

                jTable1.setModel(dtm);
                Notifications.getInstance().show(Notifications.Type.SUCCESS, Notifications.Location.TOP_RIGHT, 3500l, "GRN added Successfully!");
                normalReset();
                calTotal();
                getBalance();

            } catch (Exception e) {
                e.printStackTrace();
            }

        }

    }//GEN-LAST:event_addtogrnActionPerformed

