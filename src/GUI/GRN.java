    // cal total
    private void calTotal() {

        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();

        double total = 0;

        for (int i = 0; i < dtm.getRowCount(); i++) {
            total += Double.parseDouble(String.valueOf(jTable1.getValueAt(i, 5)));
        }

        total_lable.setText(String.valueOf(total));

    }

    // enter the paying price and get a balance
    private void getBalance() {

        String paying = paying_price.getText();

        if (!paying.isEmpty()) {

            if (!paying.matches("^[0-9]+(\\.[0-9]{1,2})?$")) {
                Notifications.getInstance().show(Notifications.Type.ERROR, Notifications.Location.TOP_RIGHT, 3500l, "Invalid GRN Material Price!");
            } else {

                Double payingValue = Double.parseDouble(paying);
                Double totalValue = Double.parseDouble(total_lable.getText());

                Double balance = totalValue - payingValue;
                balance_label.setText(String.valueOf(balance));

            }

        } else {
            paying_price.setText("0");

            Double payingValue = Double.parseDouble(paying_price.getText());
            Double totalValue = Double.parseDouble(total_lable.getText());

            Double balance = totalValue - payingValue;
            balance_label.setText(String.valueOf(balance));
        }

    }

