    private void savegrnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_savegrnActionPerformed

        // save the grn
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();

        if (dtm.getRowCount() == 0) {
            Notifications.getInstance().show(Notifications.Type.ERROR, Notifications.Location.TOP_RIGHT, 3500l, "Please add a GRN to the Table!");
        } else {

            String employee_email = jLabel3.getText();
            String grn_number = jLabel6.getText();
            String paying = paying_price.getText();
            String supplier_email = jLabel5.getText();

            if (employee_email.isEmpty()) {
                Notifications.getInstance().show(Notifications.Type.ERROR, Notifications.Location.TOP_RIGHT, 3500l, "Something went wrong!");
            } else if (grn_number.isEmpty()) {
                Notifications.getInstance().show(Notifications.Type.ERROR, Notifications.Location.TOP_RIGHT, 3500l, "Something went wrong!");
            } else if (paying.isEmpty() || paying.equals("0") || paying.equals("0.00")) {
                Notifications.getInstance().show(Notifications.Type.ERROR, Notifications.Location.TOP_RIGHT, 3500l, "Please enter the Paying Price!");
            } else if (!paying.matches("^[0-9]+(\\.[0-9]{1,2})?$")) {
                Notifications.getInstance().show(Notifications.Type.ERROR, Notifications.Location.TOP_RIGHT, 3500l, "Invalid Price!");
            } else {

                double payingValue = Double.parseDouble(paying);

                try {

                    ResultSet resultSet = MySQL.executeSearch("SELECT * FROM `grn` WHERE `id` = '" + grn_number + "'");

                    if (resultSet.next()) {
                        Notifications.getInstance().show(Notifications.Type.ERROR, Notifications.Location.TOP_RIGHT, 3500l, "Something went wrong!");
                    } else {

                        String date = new SimpleDateFormat("yyyy-MM-dd").format(new Date());

                        MySQL.executeIUD("INSERT INTO `grn` (`id`,`date`,`paid_amount`,`supplier_email`,`employee_email`) VALUES "
                                + "('" + grn_number + "', '" + date + "', '" + payingValue + "', '" + supplier_email + "', '" + employee_email + "')");

                        ResultSet resultSet1 = MySQL.executeSearch("SELECT * FROM `stock`");

                        for (int i = 0; i < dtm.getRowCount(); i++) {

                            String material_id = String.valueOf(jTable1.getValueAt(i, 1));
                            String grn_material_qty = String.valueOf(jTable1.getValueAt(i, 3));
                            double grn_material_qty_value = Double.parseDouble(grn_material_qty);

                            boolean insert = true;

                            while (resultSet1.next()) {

                                if (material_id.equals(resultSet1.getString("material_id"))) {

                                    MySQL.executeIUD("UPDATE `stock` SET `qty` = `qty` + '" + grn_material_qty_value + "' WHERE `material_id` = '" + material_id + "'");
                                    insert = false;

                                }

                            }

                            if (insert) {

                                MySQL.executeIUD("INSERT INTO `stock` (`material_id`,`qty`) VALUES ('" + material_id + "', '" + grn_material_qty_value + "')");

                            }

                        }

                        for (int i = 0; i < dtm.getRowCount(); i++) {

                            String m_id = String.valueOf(jTable1.getValueAt(i, 1));
                            String grn_m_qty = String.valueOf(jTable1.getValueAt(i, 3));
                            String grn_m_uprice = String.valueOf(jTable1.getValueAt(i, 4));

                            ResultSet resultSet2 = MySQL.executeSearch("SELECT * FROM `stock` WHERE `material_id` = '" + m_id + "'");

                            String stock_id = null;

                            if (resultSet2.next()) {
                                stock_id = resultSet2.getString("id");
                            }

                            MySQL.executeIUD("INSERT INTO `grn_item` (`qty`,`price`,`material_id`,`stock_id`,`grn_id`) VALUES "
                                    + "('" + grn_m_qty + "', '" + grn_m_uprice + "', '" + m_id + "', '" + stock_id + "', '" + grn_number + "')");

                        }

                        Notifications.getInstance().show(Notifications.Type.SUCCESS, Notifications.Location.TOP_RIGHT, 3500l, "GRN saved Successfully!");
                        normalReset();
                        fullReset();
                        generateGRNId();
                        products.loadStock();

                    }

                } catch (SQLException e) {
                    e.printStackTrace();
                } catch (Exception e) {
                    e.printStackTrace();
                }

            }

        }
    }//GEN-LAST:event_savegrnActionPerformed

